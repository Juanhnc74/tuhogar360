// Conectar a la base de datos (reemplaza los valores con los tuyos)
$servername = "localhost";
$username = "root";
$dbname = "tuhogar";

$conn = new mysqli($servername, $username, $dbname );

// Verificar la conexión
if ($conn->connect_error) {
    die("Error de conexión a la base de datos: " . $conn->connect_error);
}

// Verificar reCAPTCHA
$recaptcha_secret = "TU_CLAVE_SECRETA_RECAPTCHA";
$recaptcha_response = $_POST['g-recaptcha-response'];

$recaptcha_url = 'https://www.google.com/recaptcha/api/siteverify';
$recaptcha_data = [
    'secret' => $recaptcha_secret,
    'response' => $recaptcha_response,
];

$options = [
    'http' => [
        'header' => "Content-type: application/x-www-form-urlencoded\r\n",
        'method' => 'POST',
        'content' => http_build_query($recaptcha_data),
    ],
];

$context = stream_context_create($options);
$recaptcha_result = file_get_contents($recaptcha_url, false, $context);
$recaptcha_data = json_decode($recaptcha_result);

if (!$recaptcha_data->success) {
    die("reCAPTCHA no válido. Vuelve e inténtalo de nuevo.");
}

// Procesar el formulario de inicio de sesión
$username = $_POST['username'];
$password = $_POST['password'];

$sql = "SELECT * FROM usuarios WHERE username = '$username' AND password = '$password'";
$result = $conn->query($sql);

if ($result->num_rows > 0) {
    echo "Inicio de sesión exitoso para $username";
} else {
    echo "Credenciales incorrectas";
}

$conn->close();
?>
